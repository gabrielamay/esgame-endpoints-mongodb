CREATE TABLE T_ESG_SELO (
    ID_SELO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(255 CHAR) NOT NULL,
    COR VARCHAR2(50 CHAR),
    PONTUACAO_MINIMA NUMBER
);

CREATE TABLE T_ESG_USUARIO_SELO (
    T_ESG_USUARIO_ID_USUARIO NUMBER NOT NULL,
    T_ESG_SELO_ID_SELO NUMBER NOT NULL,
    DATA_CONQUISTA DATE,
    PRIMARY KEY (T_ESG_USUARIO_ID_USUARIO, T_ESG_SELO_ID_SELO),
    FOREIGN KEY (T_ESG_USUARIO_ID_USUARIO) REFERENCES T_ESG_USUARIO(ID_USUARIO),
    FOREIGN KEY (T_ESG_SELO_ID_SELO) REFERENCES T_ESG_SELO(ID_SELO)
);
