CREATE TABLE T_ESG_REGISTRO_ATIVIDADE (
    ID_REGISTRO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    T_ESG_USUARIO_ID_USUARIO NUMBER NOT NULL,
    T_ESG_MISSAO_ID_MISSAO NUMBER,
    T_ESG_REPRESENTANTE_ID_REPRESENTANTE NUMBER,
    T_ESG_MATERIAL_ID_MATERIAL NUMBER,
    QUANTIDADE NUMBER,
    PONTOS_GERADOS NUMBER,
    DATA_REGISTRO DATE,

    -- Constraints de FK
    CONSTRAINT FK_REGISTRO_USUARIO FOREIGN KEY (T_ESG_USUARIO_ID_USUARIO)
        REFERENCES T_ESG_USUARIO(ID_USUARIO),

    CONSTRAINT FK_REGISTRO_MISSAO FOREIGN KEY (T_ESG_MISSAO_ID_MISSAO)
        REFERENCES T_ESG_MISSAO(ID_MISSAO),

    CONSTRAINT FK_REGISTRO_REPRESENTANTE FOREIGN KEY (T_ESG_REPRESENTANTE_ID_REPRESENTANTE)
        REFERENCES T_ESG_REPRESENTANTE(ID_REPRESENTANTE),

    CONSTRAINT FK_REGISTRO_MATERIAL FOREIGN KEY (T_ESG_MATERIAL_ID_MATERIAL)
        REFERENCES T_ESG_MATERIAL(ID_MATERIAL)
);
